package model.bean;

public class ProdutoBean {

    int id;
    String nome;
    String marca;
    String ano;
    String descricao;

    public ProdutoBean(String nome, String marca, String ano, String descricao) {
        this.nome = nome;
        this.marca = marca;
        this.ano = ano;
        this.descricao = descricao;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getAno() {
        return ano;
    }

    public void setAno(String ano) {
        this.ano = ano;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }
}
